package org.plugin.cloud.request;

public class FDDetails {
	public String fdNumber;
	public String fdName;
	public String fdDepDate;
	public String fdDepAmt;
	public String fdMatDate;
	public String fdMatAmt;
	public String userID;
	public String getFdNumber() {
		return fdNumber;
	}
	public void setFdNumber(String fdNumber) {
		this.fdNumber = fdNumber;
	}
	public String getFdName() {
		return fdName;
	}
	public void setFdName(String fdName) {
		this.fdName = fdName;
	}
	public String getFdDepDate() {
		return fdDepDate;
	}
	public void setFdDepDate(String fdDepDate) {
		this.fdDepDate = fdDepDate;
	}
	public String getFdDepAmt() {
		return fdDepAmt;
	}
	public void setFdDepAmt(String fdDepAmt) {
		this.fdDepAmt = fdDepAmt;
	}
	public String getFdMatDate() {
		return fdMatDate;
	}
	public void setFdMatDate(String fdMatDate) {
		this.fdMatDate = fdMatDate;
	}
	public String getFdMatAmt() {
		return fdMatAmt;
	}
	public void setFdMatAmt(String fdMatAmt) {
		this.fdMatAmt = fdMatAmt;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
}
