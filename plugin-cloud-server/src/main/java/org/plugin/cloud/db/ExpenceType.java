package org.plugin.cloud.db;

public class ExpenceType {
	public Integer expenceID;
	public String expenceName;
	public String expenceStatus;

	public Integer getExpenceID() {
		return expenceID;
	}
	public void setExpenceID(Integer expenceID) {
		this.expenceID = expenceID;
	}
	public String getExpenceName() {
		return expenceName;
	}
	public void setExpenceName(String expenceName) {
		this.expenceName = expenceName;
	}
	public String getExpenceStatus() {
		return expenceStatus;
	}
	public void setExpenceStatus(String expenceStatus) {
		this.expenceStatus = expenceStatus;
	}
}
