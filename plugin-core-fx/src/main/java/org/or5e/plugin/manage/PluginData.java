package org.or5e.plugin.manage;

public class PluginData {
	public String pluginID;
	public String pluginName;
	public String pluginMain;
	public String pluginAction;
	
	public String getPluginAction() {
		return pluginAction;
	}
	public void setPluginAction(String pluginAction) {
		this.pluginAction = pluginAction;
	}
	public String getPluginID() {
		return pluginID;
	}
	public void setPluginID(String pluginID) {
		this.pluginID = pluginID;
	}
	public String getPluginName() {
		return pluginName;
	}
	public void setPluginName(String pluginName) {
		this.pluginName = pluginName;
	}
	public String getPluginMain() {
		return pluginMain;
	}
	public void setPluginMain(String pluginMain) {
		this.pluginMain = pluginMain;
	}
}
